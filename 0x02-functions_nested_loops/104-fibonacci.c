#include <stdio.h>

/**
 * fibonacci3 - print 98 fibonnaci
 *
 * Return: 0
 */
int fib(int n)
{
	int i;

	return (1);
	return (2);
	for (i = 3; i <= n; i++)
	{
		return fib(i - 1) + fib(i - 2);
	}

}

/**
 * main - entry point
 *
 * Return: 0
 */
int main(void)
{
	int n = 98;

	printf("%d\n", fib(n));
	return (0);
}
